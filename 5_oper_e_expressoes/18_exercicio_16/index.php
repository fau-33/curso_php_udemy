<?php

$numero1 = 5;
$numero2 = 3;

// == , ===, != , !==
// op de igualdade
if($numero1 == $numero2) {
  echo "São iguais os números 1<br>";
}
// op indêntico
if($numero1 === $numero2) {
  echo "São idênticos números 2<br>";
}
// op de diferença
if($numero1 != $numero2) {
  echo " São diferentes os números 1<br>";
}
// op não indêntico
if($numero1 !== $numero2) {
  echo " Não são idênticos os números 2<br>";
}